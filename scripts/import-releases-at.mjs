async function main() {
  const userRes = await pool.query('SELECT id FROM users WHERE email = $1', [AUTHOR_EMAIL])
  if (userRes.rows.length === 0) { console.error('User not found:', AUTHOR_EMAIL); await pool.end(); process.exit(1) }
  const userId = userRes.rows[0].id
  console.log('User ID:', userId)

  const existingRes = await pool.query('SELECT title, slug FROM releases')
  const existingTitles = existingRes.rows.map(r => r.title)
  const existingSlugs = existingRes.rows.map(r => r.slug)
  console.log('Existing releases:', existingRes.rows.length)

  for (const rel of RELEASES) {
    if (existingTitles.includes(rel.title)) {
      console.log('SKIP (exists):', rel.title)
      continue
    }

    let slug = generateSlug(rel.title)
    if (existingSlugs.includes(slug)) slug = slug + '-2'

    const res = await pool.query(
      `INSERT INTO releases (title, slug, genre, annotation, authors, status)
       VALUES ($1, $2, $3, $4, $5::jsonb, $6::release_status)
       RETURNING id, title, slug, status`,
      [rel.title, slug, rel.genre, rel.annotation, JSON.stringify([{ name: 'Адиом Тимур', role: 'author' }]), rel.status]
    )

    const releaseId = res.rows[0].id
    console.log('CREATED:', res.rows[0].title, '|', res.rows[0].slug, '|', res.rows[0].status)

    await pool.query(
      `INSERT INTO release_collaborators (release_id, user_id, role) VALUES ($1, $2, 'owner'::collaborator_role)`,
      [releaseId, userId]
    )
    console.log('  collaborator added:', userId)

    existingTitles.push(rel.title)
    existingSlugs.push(slug)
  }

  console.log('\nDone!')
  await pool.end()
}

main().catch(e => { console.error(e); pool.end(); process.exit(1) })
